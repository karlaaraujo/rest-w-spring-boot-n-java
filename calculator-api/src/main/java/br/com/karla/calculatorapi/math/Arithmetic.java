package br.com.karla.calculatorapi.math;

public class Arithmetic {

    public double sum(
            Double firstNumber,
            Double secondNumber
    )  {
        return firstNumber + secondNumber;
    }

    public double subtract(
            Double firstNumber,
            Double secondNumber
    )  {
        return firstNumber - secondNumber;
    }

    public double multiply(
            Double firstNumber,
            Double secondNumber
    )  {
        return firstNumber * secondNumber;
    }

    public double divide(
            Double firstNumber,
            Double secondNumber
    )  {
        return firstNumber / secondNumber;
    }
}
